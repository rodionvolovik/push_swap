char	*ft_strcat(char *s1, const char *s2)
{
	char		*cs1;
	const char	*cs2;

	cs1 = s1;
	cs2 = s2;
	while (*cs1)
		cs1++;
	while (*cs2)
		*cs1++ = *cs2++;
	*cs1 = 0;
	return (s1);
}
